package de.coderarea.jrdp.protocol.ASN1;

/**
 * 30 Encoding the restricted character string types
 *
 * @author Sascha Biedermann
 */
public abstract class ASN1NumericString extends ASN1Object {
    private String value;

    public ASN1NumericString() {
    }

    public ASN1NumericString(String value) {
        this.value = value;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    @Override
    public String toString() {
        return "ASN1NumericString{" +
                "value='" + value + '\'' +
                '}';
    }
}
